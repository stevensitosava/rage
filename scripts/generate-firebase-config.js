// Raffy Gelato — Firebase Config Generator
// Reads env vars (from .env locally, from Vercel env vars in production)
// and writes site/js/firebase-config.js
// No external dependencies — uses only built-in Node.js modules.

const fs   = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');

/* ── Load .env file (local dev only) ───────────────────────── */
function loadEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};
  return fs.readFileSync(filePath, 'utf-8')
    .split('\n')
    .filter(line => line.trim() && !line.startsWith('#'))
    .reduce((acc, line) => {
      const eqIdx = line.indexOf('=');
      if (eqIdx === -1) return acc;
      const key = line.slice(0, eqIdx).trim();
      const val = line.slice(eqIdx + 1).trim().replace(/^["']|["']$/g, '');
      acc[key] = val;
      return acc;
    }, {});
}

// process.env (Vercel) takes precedence over .env file (local)
const fromFile = loadEnvFile(path.join(ROOT, '.env'));
const env = { ...fromFile, ...process.env };

/* ── Validate required vars ─────────────────────────────────── */
const REQUIRED = [
  'FIREBASE_API_KEY',
  'FIREBASE_AUTH_DOMAIN',
  'FIREBASE_PROJECT_ID',
  'FIREBASE_STORAGE_BUCKET',
  'FIREBASE_MESSAGING_SENDER_ID',
  'FIREBASE_APP_ID',
];

const missing = REQUIRED.filter(k => !env[k] || env[k].startsWith('YOUR_'));
if (missing.length) {
  console.error('❌  Missing Firebase env vars:', missing.join(', '));
  console.error('    Set them in .env (local) or in your Vercel project settings.');
  process.exit(1);
}

/* ── Generate firebase-config.js ────────────────────────────── */
const content = `// Raffy Gelato — Firebase Configuration
// AUTO-GENERATED by scripts/generate-firebase-config.js
// Do not edit this file directly — edit .env (local) or Vercel env vars instead.

const _firebaseConfig = {
  apiKey:            "${env.FIREBASE_API_KEY}",
  authDomain:        "${env.FIREBASE_AUTH_DOMAIN}",
  projectId:         "${env.FIREBASE_PROJECT_ID}",
  storageBucket:     "${env.FIREBASE_STORAGE_BUCKET}",
  messagingSenderId: "${env.FIREBASE_MESSAGING_SENDER_ID}",
  appId:             "${env.FIREBASE_APP_ID}"
};

if (typeof firebase !== 'undefined' && !firebase.apps.length) {
  firebase.initializeApp(_firebaseConfig);
}

// Global Firebase service references used by page-loader.js and admin.js
const db      = firebase.firestore();
const storage = firebase.storage();
const auth    = firebase.auth();
`;

const outPath = path.join(ROOT, 'site', 'js', 'firebase-config.js');
fs.writeFileSync(outPath, content, 'utf-8');
console.log('✅  firebase-config.js generated at', path.relative(ROOT, outPath));
